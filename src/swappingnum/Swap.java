package swappingnum;

public class Swap {
	public static void main(String[] args) {
		
	System.out.println("before swapping");
	 int a= 5;
	 int b=10;
	 int c;
	 System.out.println("a="+a);
	 System.out.println("b="+b);
	 c=a;//c=10
	 a=b;//c=5
	 b=c;//a=5
	 System.out.println("after swapping");
	 System.out.println("a="+a);
	 System.out.println("b="+b);
	 
	}
}
